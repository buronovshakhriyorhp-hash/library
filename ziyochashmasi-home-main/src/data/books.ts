export const getFeaturedBooks = () => booksData.filter((b) => b.isFeatured);

export const getBookById = (id: number) => booksData.find((b) => b.id === id);

export const getBooksByCategory = (categoryId: number) =>
  booksData.filter((b) => b.categoryId === categoryId);

export const searchBooks = (query: string) => {
  const q = query.toLowerCase();
  return booksData.filter(
    (b) =>
      b.title.toLowerCase().includes(q) ||
      b.author.toLowerCase().includes(q) ||
      b.category?.toLowerCase().includes(q)
  );
};

export const formatPrice = (price: number): string =>
  price.toLocaleString("uz-UZ") + " UZS";
